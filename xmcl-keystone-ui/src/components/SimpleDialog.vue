<template>
  <v-dialog
    :model-value="value"
    :persistent="persistent"
    :width="width"
    @update:model-value="emit('input', $event)"
  >
    <v-card>
      <v-card-title
        class="select-none"
        primary-title
      >
        {{ title }}
      </v-card-title>

      <v-card-text
        class="select-none"
      >
        <slot />
      </v-card-text>

      <v-divider />
      <v-card-actions>
        <v-btn
          variant="text"
          @click="onCancel"
        >
          {{ t('delete.no') }}
        </v-btn>
        <v-spacer />
        <v-btn
          :color="color ?? 'error'"
          variant="text"
          @click="onConfirm"
        >
          <v-icon start>
            {{ confirmIcon ?? 'delete' }}
          </v-icon>
          {{ confirm ?? t('delete.yes') }}
        </v-btn>
      </v-card-actions>
    </v-card>
  </v-dialog>
</template>

<script lang="ts" setup>
const props = defineProps<{
  title: string
  width?: number
  color?: string
  confirm?: string
  confirmIcon?: string
  persistent?: boolean
  value?: boolean
}>()

const { t } = useI18n()

const emit = defineEmits(['confirm', 'cancel', 'input'])

const onConfirm = () => {
  emit('confirm')
}

const onCancel = () => {
  emit('input', false)
  emit('cancel')
}

const onKeypress = (e: KeyboardEvent) => {
  if (e.key === 'Escape') {
    onCancel()
  }
}

onMounted(() => {
  document.addEventListener('keydown', onKeypress)
})
onUnmounted(() => {
  document.removeEventListener('keydown', onKeypress)
})

</script>
